def _git(checkout: Path, *args: str) -> str:
    result = subprocess.run(
        ["git", *args],
        cwd=checkout,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        check=False,
    )
    if result.returncode:
        raise ValueError(f"git {' '.join(args)} failed in {checkout}: {result.stderr.strip()}")
    return result.stdout.strip()


def _checkout_for_commit(checkout_root: Path, commit: str) -> Path:
    for candidate in sorted(checkout_root.iterdir()):
        if candidate.is_dir() and (candidate / ".git").exists() and _git(candidate, "rev-parse", "HEAD") == commit:
            return candidate
    raise ValueError(f"no checkout under {checkout_root} is pinned to {commit}")


def _generated(path: Path) -> bool:
    lowered = path.as_posix().lower()
    if any(marker in lowered for marker in ("/generated/", ".generated.", ".g.cs", ".min.")):
        return True
    try:
        prefix = path.read_text(encoding="utf-8")[:2048].lower()
    except (OSError, UnicodeError):
        return True
    return any(marker in prefix for marker in ("@generated", "code generated", "do not edit this file"))


def _candidate_paths(checkout: Path, source: dict[str, Any]) -> dict[str, list[Path]]:
    excluded = [str(item).lower() for item in source.get("exclude") or []]
    candidates: dict[str, Path] = {}
    for pattern in source.get("include") or []:
        for path in checkout.glob(str(pattern)):
            if not path.is_file():
                continue
            relative = path.relative_to(checkout).as_posix()
            lowered = relative.lower()
            if any(token in lowered for token in excluded) or _generated(path):
                continue
            try:
                lines = line_count(path.read_text(encoding="utf-8"))
                target_length_bucket(lines)
            except (OSError, UnicodeError, ValueError):
                continue
            candidates[relative] = path
    grouped: dict[str, list[Path]] = {bucket: [] for bucket in ("short", "normal", "medium", "long", "oversized")}
    for relative, path in candidates.items():
        grouped[target_length_bucket(line_count(path.read_text(encoding="utf-8")))].append(path)
    return grouped


def _stable_candidates(paths: Iterable[Path], *, checkout: Path, seed: int, snapshot_id: str) -> list[Path]:
    return sorted(
        paths,
        key=lambda path: sha256(
            f"{seed}:{snapshot_id}:{path.relative_to(checkout).as_posix()}".encode("utf-8")
        ).hexdigest(),
    )


def _copy_snapshot_file(checkout: Path, snapshot_root: Path, relative: str) -> dict[str, Any]:
    source = checkout / relative
    data = source.read_bytes()
    destination = snapshot_root / "tree" / relative
    destination.parent.mkdir(parents=True, exist_ok=True)
    destination.write_bytes(data)
    return {
        "sha256": sha256(data).hexdigest(),
        "bytes": len(data),
        "lines": line_count(data.decode("utf-8")),
        "git_blob": _git(checkout, "rev-parse", f"HEAD:{relative}"),
    }


def _schedule_slots(
    selected: list[SelectedSource], snapshot_files: dict[str, list[str]], contexts: dict[str, str], seed: int
) -> list[dict[str, Any]]:
    families = [family for family, count in FAMILY_COUNTS.items() for _ in range(count)]
    if not (len(selected) == len(families) == 100):
        raise ValueError("mechanical corpus schedules must each contain exactly 100 entries")
    rng = random.Random(seed)
    rng.shuffle(families)
    slots: list[dict[str, Any]] = []
    by_snapshot_index: Counter[str] = Counter()
    by_family_index: Counter[str] = Counter()

    def dimensions(family: str, occurrence: int) -> tuple[int, int]:
        if family == "localized-replacement":
            return 1, 1
        if family == "block-insertion":
            return (1 if occurrence <= 10 else 2), (1 if occurrence <= 8 else 2 + occurrence % 2)
        if family == "targeted-removal":
            return (1 if occurrence <= 8 else 2), (1 if occurrence <= 7 else 2 + occurrence % 2)
        if family == "ambiguous-context":
            return 1, 2 + occurrence % 2
        if family == "nested-file-creation":
            return 2 + occurrence % 2, 2 + occurrence % 2
        if family == "same-file-multi-hunk":
            return 1, (2 + occurrence % 2 if occurrence <= 10 else occurrence - 7)
        if family == "signature-callsite":
            return 2 + occurrence % 2, (4 + (occurrence - 1) % 5 if occurrence <= 10 else occurrence - 2)
        if family == "coordinated-multi-file":
            files = 2 + occurrence % 2 if occurrence <= 9 else 4 + (occurrence - 10) % 5
            operations = 4 + (occurrence - 1) % 5 if occurrence <= 12 else occurrence - 4
            return files, operations
        if family == "structured-data":
            files = [4, 4, 5, 5, 6, 6, 7, 8][occurrence - 1]
            operations = min(8, 3 + occurrence) if occurrence <= 6 else occurrence + 6
            return files, operations
        raise ValueError(f"unknown mechanical edit family: {family}")

    for index, primary in enumerate(selected, 1):
        family = families[index - 1]
        family_occurrence = by_family_index[family] + 1
        by_family_index[family] = family_occurrence
        requested_files, requested_operations = dimensions(family, family_occurrence)
        available = snapshot_files[primary.snapshot_id]
        position = available.index(primary.path)
        existing_targets = requested_files - 1 if family == "nested-file-creation" else requested_files
        target_files = [available[(position + offset) % len(available)] for offset in range(existing_targets)]
        target_files = list(dict.fromkeys(target_files))
        if len(target_files) < existing_targets:
            raise ValueError(f"snapshot {primary.snapshot_id} cannot provide {existing_targets} distinct target files")
        workspace_files = list(target_files)
        context = contexts[primary.snapshot_id]
        if family == "structured-data":
            if context not in target_files:
                target_files[-1] = context
            workspace_files = list(target_files)
        local_index = by_snapshot_index[primary.snapshot_id] + 1
        by_snapshot_index[primary.snapshot_id] = local_index
        slots.append(
            {
                "id": f"mechanical-{primary.language}-{local_index:02d}",
                "snapshot_id": primary.snapshot_id,
                "language": primary.language,
                "family": family,
                "primary_target": primary.path,
                "target_length": primary.bucket,
                "target_files": target_files,
                "workspace_files": workspace_files,
                "requested_target_files": requested_files,
                "requested_operations": requested_operations,
                "seed": seed + index,
            }
        )
    file_groups = Counter(
        "single" if slot["requested_target_files"] == 1 else "few" if slot["requested_target_files"] <= 3 else "many"
        for slot in slots
    )
    operation_groups = Counter(
        "one"
        if slot["requested_operations"] == 1
        else "few"
        if slot["requested_operations"] <= 3
        else "several"
        if slot["requested_operations"] <= 8
        else "many"
        for slot in slots
    )
    if file_groups != {"single": 50, "few": 35, "many": 15}:
        raise ValueError(f"file-scope schedule drifted: {dict(file_groups)}")
    if operation_groups != {"one": 25, "few": 35, "several": 30, "many": 10}:
        raise ValueError(f"operation schedule drifted: {dict(operation_groups)}")
    return slots


def import_sources(
    config_path: Path, checkout_root: Path, package_root: Path
) -> tuple[list[dict[str, Any]], list[SelectedSource]]:
    """Import a deterministic, hash-verified subset of pinned public repositories."""
    config = yaml.safe_load(config_path.read_text(encoding="utf-8"))
    seed = int(config["seed"])
    snapshots_root = package_root / "fixtures" / "snapshots"
    selected: list[SelectedSource] = []
    snapshot_files: dict[str, list[str]] = {}
    contexts: dict[str, str] = {}
    for source in config["sources"]:
        snapshot_id = str(source["id"])
        checkout = _checkout_for_commit(checkout_root, str(source["commit"]))
        if _git(checkout, "remote", "get-url", "origin") != source["repository"]:
            raise ValueError(f"repository URL mismatch for {snapshot_id}")
        grouped = _candidate_paths(checkout, source)
        chosen: list[Path] = []
        for bucket, count in source["quotas"].items():
            candidates = _stable_candidates(grouped[bucket], checkout=checkout, seed=seed, snapshot_id=snapshot_id)
            if len(candidates) < int(count):
                raise ValueError(
                    f"{snapshot_id} needs {count} {bucket} files but only {len(candidates)} eligible files exist"
                )
            chosen.extend(candidates[: int(count)])
        snapshot_root = snapshots_root / snapshot_id
        if snapshot_root.exists():
            shutil.rmtree(snapshot_root)
        files: dict[str, Any] = {}
        selected_paths: list[str] = []
        for path in chosen:
            relative = path.relative_to(checkout).as_posix()
            files[relative] = _copy_snapshot_file(checkout, snapshot_root, relative)
            selected_paths.append(relative)
            selected.append(
                SelectedSource(
                    snapshot_id=snapshot_id,
                    language=str(source["language"]),
                    path=relative,
                    bucket=target_length_bucket(files[relative]["lines"]),
                )
            )
        context = str(source["context_file"])
        if context not in files:
            files[context] = _copy_snapshot_file(checkout, snapshot_root, context)
        contexts[snapshot_id] = context
        licenses: list[str] = []
        for license_path in source["license_files"]:
            relative = str(license_path)
            destination = snapshot_root / "licenses" / Path(relative).name
            destination.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(checkout / relative, destination)
            licenses.append(destination.relative_to(snapshot_root).as_posix())
        manifest = {
            "schema_version": 1,
            "id": snapshot_id,
            "language": source["language"],
            "repository": source["repository"],
            "commit": source["commit"],
            "license": source["license"],
            "license_files": licenses,
            "files": dict(sorted(files.items())),
        }
        (snapshot_root / "manifest.yaml").write_text(
            yaml.safe_dump(manifest, sort_keys=False, allow_unicode=True), encoding="utf-8"
        )
        snapshot_files[snapshot_id] = selected_paths

    slots = _schedule_slots(selected, snapshot_files, contexts, seed)
    slots_path = package_root / "fixtures" / "authoring-slots.yaml"
    slots_path.parent.mkdir(parents=True, exist_ok=True)
    slots_path.write_text(yaml.safe_dump(slots, sort_keys=False, allow_unicode=True), encoding="utf-8")
    return slots, selected


def _load_slot_before(package_root: Path, slot: dict[str, Any]) -> dict[str, FileContent]:
    snapshot_root = package_root / "fixtures" / "snapshots" / slot["snapshot_id"]
    manifest = yaml.safe_load((snapshot_root / "manifest.yaml").read_text(encoding="utf-8"))
    before: dict[str, FileContent] = {}
    for relative in slot["workspace_files"]:
        metadata = manifest["files"][relative]
        data = (snapshot_root / "tree" / relative).read_bytes()
        if sha256(data).hexdigest() != metadata["sha256"]:
            raise ValueError(f"snapshot hash mismatch for {slot['snapshot_id']}/{relative}")
        before[relative] = FileContent(text=data.decode("utf-8"))
    return before


def _group_count(value: int) -> str:
    if value == 1:
        return "one"
    if value <= 3:
        return "few"
    if value <= 8:
        return "several"
    return "many"


def _file_group_count(value: int) -> str:
    if value == 1:
        return "one"
    if value <= 3:
        return "few"
    return "many"


def _authoring_prompt(slot: dict[str, Any], created_path: str | None) -> str:
    files = "\n".join(f"- `{path}`" for path in slot["target_files"])
    file_group = _file_group_count(int(slot["requested_target_files"]))
    family_guidance = {
        "localized-replacement": (
            "Make exactly one contiguous replacement with a plausible improvement. "
            "Do not add an import or touch any second region."
        ),
        "block-insertion": "Add new code in separate insertion regions; avoid rewriting surrounding blocks.",
        "targeted-removal": "Remove obsolete code regions without reformatting their neighbors.",
        "nested-file-creation": f"Create `{created_path}` and integrate it from every listed existing target file.",
        "same-file-multi-hunk": "Make several separated edits in the one target file.",
        "signature-callsite": "Change a declaration and update supplied call-site-like regions consistently.",
        "coordinated-multi-file": "Make a consistent mechanical change in every supplied target file.",
        "ambiguous-context": "Edit repeated-looking regions while preserving neighboring occurrences.",
        "structured-data": "Update the supplied configuration file and its related source locations consistently.",
    }[slot["family"]]
    creation_rule = (
        f"Create exactly one new file, `{created_path}`. " if created_path is not None else "Do not create files. "
    )
    return f"""Create one candidate edit for a mechanical edit-tool benchmark.

This is corpus authoring, not a benchmark trial. Make an idiomatic, plausible,
syntax-valid change using the available file-edit tools. Do not merely describe
the change and do not add tests, generated output, or benchmark commentary.

Family: {slot["family"]}
Primary target: `{slot["primary_target"]}`
Existing target files:
{files}

{family_guidance}

Change every listed target file and no other existing file. {creation_rule}
The accepted diff must contain {_group_count(int(slot["requested_operations"]))} independent edit regions
(requested count {slot["requested_operations"]}) across {file_group} changed files
(requested count {slot["requested_target_files"]}). Keep distinct edit regions
separated by unchanged lines. Avoid whole-file rewrites, broad formatting, and
changes that require dependencies not already visible in these files.

When the workspace is correct, stop.
"""


def _created_path(slot: dict[str, Any]) -> str | None:
    if slot["family"] != "nested-file-creation":
        return None
    primary = Path(slot["primary_target"])
    extension = LANGUAGE_EXTENSIONS[slot["language"]]
    return (primary.parent / f"mechanical_support_{slot['seed']}{extension}").as_posix()


def _error_nodes(node: Any) -> int:
    count = int(node.type == "ERROR" or node.is_missing)
    return count + sum(_error_nodes(child) for child in node.children)


def _syntax_errors(language: str, text: str) -> int:
    parser_name = LANGUAGE_PARSERS[language]
    try:
        parser = get_parser(cast(Any, parser_name))
    except Exception:  # noqa: BLE001 - download errors are surfaced by the retry
        download_parser([parser_name])
        parser = get_parser(cast(Any, parser_name))
    return _error_nodes(parser.parse(text.encode("utf-8")).root_node)


def syntax_errors_for_path(language: str, path: str, text: str) -> int | None:
    """Return syntax-error count for source and common structured context files."""
    suffix = Path(path).suffix.lower()
    name = Path(path).name
    try:
        if suffix == ".json":
            json.loads(text)
            return 0
        if suffix == ".toml":
            tomllib.loads(text)
            return 0
        if suffix in {".yaml", ".yml"}:
            yaml.safe_load(text)
            return 0
        if suffix in {".xml", ".csproj"}:
            ElementTree.fromstring(text)
            return 0
    except (json.JSONDecodeError, tomllib.TOMLDecodeError, yaml.YAMLError, ElementTree.ParseError):
        return 1

    parser_language = language
    source_suffixes = {LANGUAGE_EXTENSIONS[language], ".cc"}
    if suffix == ".kts":
        parser_language = "kotlin"
    elif suffix == ".gemspec":
        parser_language = "ruby"
    elif name == "CMakeLists.txt":
        parser_language = "cmake"
    elif suffix not in source_suffixes:
        return None
    return _syntax_errors(parser_language, text)


def _actual_tree(root: Path) -> dict[str, FileContent]:
    result: dict[str, FileContent] = {}
    for path, data in read_tree(root).items():
        result[path] = FileContent(text=data.decode("utf-8"))
    return result


def _validate_candidate(
    slot: dict[str, Any],
    before: dict[str, FileContent],
    after: dict[str, FileContent],
    created_path: str | None,
) -> tuple[dict[str, Any] | None, list[str]]:
    errors: list[str] = []
    deleted = sorted(set(before) - set(after))
    if deleted:
        errors.append(f"deleted files are outside the shared protocol surface: {', '.join(deleted)}")
    changed = {path for path in set(before) | set(after) if before.get(path) != after.get(path)}
    expected_changed = set(slot["target_files"])
    if created_path is not None:
        expected_changed.add(created_path)
    if changed != expected_changed:
        errors.append(f"changed paths {sorted(changed)} do not equal requested paths {sorted(expected_changed)}")
    if slot["primary_target"] not in changed:
        errors.append("primary target was not changed")
    try:
        recipe = derive_recipe(before, after)
    except ValueError as exc:
        errors.append(str(exc))
        return None, errors
    if _group_count(len(recipe.operations)) != _group_count(int(slot["requested_operations"])):
        errors.append(
            f"operation bucket mismatch: requested {_group_count(int(slot['requested_operations']))}, "
            f"received {len(recipe.operations)}"
        )
    kinds = Counter(operation.kind for operation in recipe.operations)
    if slot["family"] == "localized-replacement" and kinds != {"replace": 1}:
        errors.append("localized-replacement task must produce exactly one replacement operation")
    if slot["family"] == "block-insertion" and not kinds["insert"]:
        errors.append("block-insertion task produced no insertion operation")
    if slot["family"] == "targeted-removal" and not kinds["delete"]:
        errors.append("targeted-removal task produced no deletion operation")
    if slot["family"] == "nested-file-creation" and kinds["create"] != 1:
        errors.append("nested-file-creation task must produce exactly one create operation")
    for path in sorted(changed):
        after_errors = syntax_errors_for_path(slot["language"], path, after[path].text)
        if after_errors is None:
            continue
        if path == created_path:
            before_errors = 0
        else:
            before_errors = syntax_errors_for_path(slot["language"], path, before[path].text) or 0
        if after_errors > before_errors:
            errors.append(f"{path} introduces {after_errors - before_errors} parser error node(s)")
    prompt = render_recipe_prompt(recipe, before)
    if any(operation.new_text and operation.new_text not in prompt for operation in recipe.operations):
        errors.append("rendered prompt omits supplied new content")
    if errors:
        return None, errors
    operation_count = len(recipe.operations)
    target_file_count = len(changed)
    difficulty = "easy" if operation_count == target_file_count == 1 else "medium" if operation_count <= 3 else "hard"
    task = {
        "id": slot["id"],
        "snapshot_id": slot["snapshot_id"],
        "workspace_files": list(before),
        "primary_target": slot["primary_target"],
        "language": slot["language"],
        "family": slot["family"],
        "difficulty": difficulty,
        "shape": "mechanical",
        "target_length": slot["target_length"],
        "payload_size": payload_size_bucket(payload_line_count(recipe, before)),
        "tags": [
            "mechanical",
            slot["language"],
            slot["family"],
            slot["target_length"],
            _group_count(operation_count) + "-operations",
            _file_group_count(target_file_count) + "-files",
        ],
        "provenance": "synthetic",
        "seed": slot["seed"],
        "generator": "mechanical-authoring-v1",
        "recipe": recipe.model_dump(mode="json"),
    }
    return task, []


async def _author_slot(
    slot: dict[str, Any],
    *,
    package_root: Path,
    artifact_root: Path,
) -> tuple[dict[str, Any] | None, list[dict[str, Any]]]:
    before = _load_slot_before(package_root, slot)
    created_path = _created_path(slot)
    attempts: list[dict[str, Any]] = []
    authoring_run = utc_now().replace("+00:00", "Z").replace(":", "").replace(".", "-")
    for attempt_number, (provider, model, protocol, effort) in enumerate(AUTHORING_ATTEMPTS, 1):
        attempt_dir = artifact_root / slot["id"] / "runs" / authoring_run / f"attempt-{attempt_number}"
        attempt_dir.mkdir(parents=True, exist_ok=True)
        with tempfile.TemporaryDirectory(prefix=f"kolega-author-{slot['id']}-") as temporary:
            workspace = Path(temporary) / "workspace"
            materialize_tree(workspace, before)
            dummy_expected = {path: content.model_copy(deep=True) for path, content in before.items()}
            primary = slot["primary_target"]
            dummy_expected[primary] = FileContent(text=dummy_expected[primary].text + " ")
            task = TaskSpec(
                id=slot["id"],
                prompt=_authoring_prompt(slot, created_path),
                before_files=before,
                expected_files=dummy_expected,
            )
            model_spec = ModelRunSpec(
                provider=provider,
                model=model,
                protocols=[protocol],
                thinking_effort=effort,
                max_output_tokens=16384,
            )
            parameters = _model_parameters(model_spec)
            parameters["max_iterations"] = 20
            trial = PlannedTrial(
                trial_id=f"author-{slot['id']}-{attempt_number}",
                task=task,
                lane="controlled",
                model=model_spec,
                protocol=protocol,
                protocol_version="authoring-v1",
                repetition=attempt_number,
                seed=int(slot["seed"]),
                model_parameters=parameters,
            )
            started = utc_now()
            try:
                config = _build_config(workspace, trial)
                execution = await _controlled_execution(workspace, attempt_dir, config, trial)
                after = _actual_tree(workspace)
                accepted, validation_errors = _validate_candidate(slot, before, after, created_path)
                write_json(attempt_dir / "transcript.json", execution.transcript)
                write_json(attempt_dir / "events.json", execution.events)
                write_json(attempt_dir / "attempts.json", [item.model_dump(mode="json") for item in execution.attempts])
            except Exception as exc:  # noqa: BLE001 - fallback is the purpose of this pipeline
                accepted = None
                validation_errors = [f"{type(exc).__name__}: {exc}"]
            attempt = {
                "task_id": slot["id"],
                "authoring_run": authoring_run,
                "attempt": attempt_number,
                "provider": provider,
                "model": model,
                "protocol": protocol,
                "started_at": started,
                "accepted": accepted is not None,
                "errors": validation_errors,
            }
            attempts.append(attempt)
            write_json(attempt_dir / "result.json", attempt)
            if accepted is not None:
                accepted["authoring"] = {
                    "provider": provider,
                    "model": model,
                    "protocol": protocol,
                    "attempt": attempt_number,
                    "prompt_template": "mechanical-authoring-v1",
                    "syntax_parser": "tree-sitter-language-pack-1.6.0",
                }
                return accepted, attempts
    return None, attempts


def _dump_tasks(tasks: list[dict[str, Any]]) -> str:
    class LiteralDumper(yaml.SafeDumper):
        pass

    def represent_string(dumper: yaml.SafeDumper, value: str) -> yaml.Node:
        style = "|" if "\n" in value else None
        return dumper.represent_scalar("tag:yaml.org,2002:str", value, style=style)

    LiteralDumper.add_representer(str, represent_string)
    return yaml.dump(tasks, Dumper=LiteralDumper, sort_keys=False, allow_unicode=True, width=120)


async def author_corpus(
    *,
    slots_path: Path,
    output_path: Path,
    package_root: Path,
    repo_root: Path,
    artifact_root: Path,
    concurrency: int = 2,
    task_ids: set[str] | None = None,
    max_tasks: int | None = None,
) -> list[dict[str, Any]]:
    """Author missing slots with a clean-workspace retry and validated fallback."""
    load_dotenv(repo_root / ".env", override=False)
    slots: list[dict[str, Any]] = yaml.safe_load(slots_path.read_text(encoding="utf-8"))
    existing: list[dict[str, Any]] = (
        yaml.safe_load(output_path.read_text(encoding="utf-8")) or [] if output_path.exists() else []
    )
    by_id: dict[str, dict[str, Any]] = {}
    slot_by_id = {slot["id"]: slot for slot in slots}
    for task in existing:
        if not task.get("recipe"):
            continue
        slot = slot_by_id[task["id"]]
        before = _load_slot_before(package_root, slot)
        recipe = EditRecipeSpec.model_validate(task["recipe"])
        after = apply_recipe(before, recipe)
        _, validation_errors = _validate_candidate(slot, before, after, _created_path(slot))
        if validation_errors:
            print(f"  ~~ {task['id']} scheduled for re-authoring: {'; '.join(validation_errors)}", flush=True)
            continue
        task["payload_size"] = payload_size_bucket(payload_line_count(recipe, before))
        operation_count = len(recipe.operations)
        target_file_count = len({operation.path for operation in recipe.operations})
        task["tags"] = [
            "mechanical",
            slot["language"],
            slot["family"],
            slot["target_length"],
            _group_count(operation_count) + "-operations",
            _file_group_count(target_file_count) + "-files",
        ]
        by_id[task["id"]] = task
    pending = [slot for slot in slots if slot["id"] not in by_id and (not task_ids or slot["id"] in task_ids)]
    if max_tasks is not None:
        pending = pending[:max_tasks]
    artifact_root.mkdir(parents=True, exist_ok=True)
    journal = artifact_root / "journal.jsonl"
    semaphore = asyncio.Semaphore(concurrency)
    write_lock = asyncio.Lock()
    slot_order = {slot["id"]: index for index, slot in enumerate(slots)}
    atomic_write_text(output_path, _dump_tasks(sorted(by_id.values(), key=lambda item: slot_order[item["id"]])))

    async def run(slot: dict[str, Any]) -> None:
        async with semaphore:
            task, attempts = await _author_slot(slot, package_root=package_root, artifact_root=artifact_root)
        async with write_lock:
            for attempt in attempts:
                append_jsonl(journal, attempt)
            if task is None:
                print(f"  !! {slot['id']} failed all authoring attempts", flush=True)
                return
            by_id[slot["id"]] = task
            ordered = sorted(by_id.values(), key=lambda item: slot_order[item["id"]])
            atomic_write_text(output_path, _dump_tasks(ordered))
            print(
                f"  -> {slot['id']} accepted with {task['authoring']['provider']}/{task['authoring']['model']} "
                f"on attempt {task['authoring']['attempt']}",
                flush=True,
            )

    await asyncio.gather(*(run(slot) for slot in pending))
    result = sorted(by_id.values(), key=lambda item: slot_order[item["id"]])
    failures = [slot["id"] for slot in pending if slot["id"] not in by_id]
    if failures:
        raise ValueError(f"authoring failed for {len(failures)} task(s): {', '.join(failures)}")
    atomic_write_text(output_path, _dump_tasks(result))
    return result
